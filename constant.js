// utility functions

// リストのappend
const appendLists = (l1, l2) => {
  for (let e of l2) l1.push(e);
  return l1;
};

// 指定した難易度の曲を取得
const getMusicsByDiff = diffList => {
  let result = [];
  for (let diff of diffList)
    appendLists(result, constantTable.filter(m => m.difficulty == diff));
  return result;
};

// バージョンにおける，特定のレベルを取得
const getMusicsByLevel = (version, lvList) => {
  let result = [];
  for (let lv of lvList)
    appendLists(result, constantTable.filter(m => m.constant[version] == lv));
  return result;
};

const difficultyToId = str =>
  str == "master" ? 3 : str == "expert" ? 2 : str == "advanced" ? 1 : 0;

const idToDifficulty = id =>
  id == 3 ? "master" : id == 2 ? "expert" : id == 1 ? "advanced" : "basic";

const versionToId = str =>
  str == "origin"
    ? 0
    : str == "plus"
    ? 1
    : str == "air"
    ? 2
    : str == "airplus"
    ? 3
    : str == "star"
    ? 4
    : str == "starplus"
    ? 5
    : DEFAULTVERSION;

// スコアが無い曲の情報を取得(主に新曲用)
const getNewMusic = mboxList => {
  let result = [];
  for (let m of mboxList) {
    // "LEVEL 13"といった要素は飛ばす
    if (m.classList[0] == "box01_title") continue;

    // プレイ済みのものは飛ばす(12以上は全曲触ってるため)
    if (m.getElementsByClassName("play_musicdata_highscore").length != 0)
      continue;

    result.push(m);
  }
  return result;
};

const getNewMusicInfo = () => {
  const musicDetail = document.getElementById("music_detail");
  let result = [];
  for (let box01 of musicDetail.children)
    appendLists(result, getNewMusic(box01.children));
  return result;
};

const diffToNum = diff => (diff == "MAS" ? 3 : 2);

const mergeConstantTable = () => {
  let output = "[\n";

  for (var item of constantTable) {
    let music_data = new_constants.find(musiclist => {
      return (
        musiclist.title == item.title &&
        diffToNum(musiclist.difficulty) == item.difficulty
      );
    });
    console.log(music_data);
    let new_constant =
      typeof music_data === "undefined" ? 0 : music_data.constant;

    // 文字列編集
    let split_target = "{";
    let s = JSON.stringify(item);
    let splited = s.split(split_target);
    output += splited[0];
    output += split_target;
    output += splited[1];
    output += split_target;
    output += "amazon:";
    output += new_constant;
    output += ",";
    output += splited[2];
    output += ",\n";
  }
  output += "]";
  return output;
};
